// T·∫°o tr√°i tim r∆°i theo s√≥ng
function createHeartWave() {
    const waveCount = 10;
    for (let i = 0; i < waveCount; i++) {
        setTimeout(() => {
            const x = (window.innerWidth / waveCount) * i + Math.random() * 50;
            const y = -50;
            createFallingHeart(x, y);
        }, i * 100);
    }
}

// T·∫°o tr√°i tim r∆°i theo v√≤ng tr√≤n
function createHeartCircle(centerX, centerY, radius = 100) {
    const heartCount = 12;
    for (let i = 0; i < heartCount; i++) {
        const angle = (360 / heartCount) * i;
        const x = centerX + Math.cos(angle * Math.PI / 180) * radius;
        const y = centerY + Math.sin(angle * Math.PI / 180) * radius;
        
        setTimeout(() => {
            createFallingHeart(x, y);
        }, i * 50);
    }
}

// T·∫°o tr√°i tim r∆°i theo h√¨nh tr√°i tim
function createHeartShape(centerX, centerY) {
    const heartPoints = [
        {x: 0, y: 0},
        {x: -20, y: -10},
        {x: -40, y: 0},
        {x: -30, y: 20},
        {x: 0, y: 40},
        {x: 30, y: 20},
        {x: 40, y: 0},
        {x: 20, y: -10}
    ];
    
    heartPoints.forEach((point, index) => {
        setTimeout(() => {
            createFallingHeart(centerX + point.x, centerY + point.y);
        }, index * 100);
    });
}

